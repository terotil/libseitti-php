  }

  // Get date
  $month      = date('n', $timestamp);
  $dayofmonth = date('j', $timestamp);
  $dayofweek  = date('w', $timestamp);
  $year       = date('Y', $timestamp );

  switch ( $language_code ) {
  case 'en':
    global $dow;
    global $months;
    // Date
    $teksti  = 'Today is ';
    $teksti .= $dow[$dayofweek] . ' ';
    $teksti .= $dayofmonth . '. ';
    $teksti .= $months[$month] . ' ';
    $teksti .= $year . '. ';
    // Nameday
    if ( $nimipaivat[$month][$dayofmonth] != '' ) {
      $teksti .= str_replace(' ja ', 
			     ' and ', 
			     $nimipaivat[$month][$dayofmonth]);
      if ( substr_count($nimipaivat[$month][$dayofmonth], ' ja ') ) {
	$teksti .= " are celebrating their namedays";
      } else {
	$teksti .= " is celebrating his nameday";
      }
      $teksti .= ', according to finnish calendar.';
    }
    break;
  default: // defaults to finnish
  case 'fi':
    global $kuukaudet;
    global $viikonpaivat;
    // Date
    $teksti  = 'Tänään on ';
    $teksti .= $viikonpaivat[$dayofweek] . ' ';
    $teksti .= $dayofmonth . '. ';
    $teksti .= $kuukaudet[$month] . 'kuuta ';
    $teksti .= $year . '. ';
    // Nameday
    if ( $nimipaivat[$month][$dayofmonth] != '' ) {
      if ( substr_count($nimipaivat[$month][$dayofmonth], ' ja ') ) {
	$teksti .= 'Nimipäiväänsä viettävät ';
      } else {
	$teksti .= 'Nimipäiväänsä viettää ';
      }
      $teksti .= $nimipaivat[$month][$dayofmonth] . '.';
    }
    break;
  }

  return $teksti;
}

